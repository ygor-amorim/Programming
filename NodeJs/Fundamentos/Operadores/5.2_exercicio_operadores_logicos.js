// Duas expressões resultar em TRUE (Aritmeticos/Relacionais/Logicos)
let verdadeira1 = 50 / 2 == 25 && 2 * 25 == 50,
    verdadeira2 = 23 > 22 || 12 <= 3;

console.log()
console.log(verdadeira1);
console.log(verdadeira2);
    

// Duas expressões resultar em FALSE (Aritmeticos/Relacionais/Logico)

let falso1 = 42 !== 42 && 3 > 2,
    falso2 = 23 > 23 || 12 == 3;

