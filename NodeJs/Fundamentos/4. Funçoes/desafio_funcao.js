// funcao: 2 parametros -> console.log(+)
// funcao: 2 parametros -> console.log(-)

function soma(a, b) {
  console.log(a + b);
}

function subtracao(a, b) {
  console.log(a - b);
}

soma(1, 2); // 3
soma(2, 2); // 4
soma(3, 2); // 5

subtracao(1, 2); // -1
subtracao(2, 2); // 0
subtracao(3, 2); // 1
