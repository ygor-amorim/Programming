// laço for com numeros de 1 a 100
for (let controle = 0; controle <= 100; controle += 1) {
  console.log(controle);
}

// laço for com todos os multiplos de 2 menores ou iguais a 100
for (controle = 2; controle <= 100; controle += 2) {
  console.log(controle);
}

