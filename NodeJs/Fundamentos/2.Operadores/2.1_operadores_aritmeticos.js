/* Os operadores aritméticos básicos são:
Adição (+), Subtração (-), Multiplicação (*),
Divisão (/) e Módulo/Resto (%)
*/

// valores literais

console.log(7 + 3);
console.log(6 - 7);
console.log(7 * 9);
console.log(8 / 3);
console.log(8 % 3);

// usando variáveis

const a = 5;
const b = 14;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
